var fieldTypeConsts = map[reportcatalog.FieldType]string{
	reportcatalog.FieldString:  "FieldString",
	reportcatalog.FieldInt:     "FieldInt",
	reportcatalog.FieldDecimal: "FieldDecimal",
	reportcatalog.FieldBool:    "FieldBool",
	reportcatalog.FieldEnum:    "FieldEnum",
	reportcatalog.FieldEpoch:   "FieldEpoch",
	reportcatalog.FieldRef:     "FieldRef",
	reportcatalog.FieldJSON:    "FieldJSON",
}

var aggregationConsts = map[reportcatalog.Aggregation]string{
	reportcatalog.AggCount:         "AggCount",
	reportcatalog.AggCountDistinct: "AggCountDistinct",
	reportcatalog.AggSum:           "AggSum",
	reportcatalog.AggAvg:           "AggAvg",
	reportcatalog.AggMin:           "AggMin",
	reportcatalog.AggMax:           "AggMax",
}

var formatConsts = map[reportcatalog.FormatHint]string{
	reportcatalog.FormatMoney:    "FormatMoney",
	reportcatalog.FormatWeight:   "FormatWeight",
	reportcatalog.FormatPercent:  "FormatPercent",
	reportcatalog.FormatDuration: "FormatDuration",
	reportcatalog.FormatDistance: "FormatDistance",
	reportcatalog.FormatCount:    "FormatCount",
}

var cardinalityConsts = map[reportcatalog.Cardinality]string{
	reportcatalog.CardinalityOne:  "CardinalityOne",
	reportcatalog.CardinalityMany: "CardinalityMany",
	reportcatalog.CardinalityM2M:  "CardinalityM2M",
}

func ComputeVersion(catalog *reportcatalog.Catalog, manifestRaw []byte) string {
	var dump strings.Builder
	dump.Write(manifestRaw)
	dump.WriteByte('\n')

	for i := range catalog.Entities {
		entity := &catalog.Entities[i]
		fmt.Fprintf(&dump, "entity %s resource=%s table=%s alias=%s pk=%v tenant=%s/%s owner=%s\n",
			entity.Key, entity.Resource, entity.Table.Name, entity.Table.Alias,
			entity.Table.PrimaryKey, entity.Tenant.OrganizationID, entity.Tenant.BusinessUnitID,
			entity.OwnershipColumn,
		)
		for j := range entity.Fields {
			field := &entity.Fields[j]
			fmt.Fprintf(&dump, "  field %s col=%s type=%s fmt=%s null=%t aggs=%v filter=%t group=%t enums=%v\n",
				field.Key, field.Column.Name, field.Type, field.Format, field.Nullable,
				field.Aggregations, field.Filterable, field.Groupable, field.EnumValues,
			)
		}
		for j := range entity.Edges {
			edge := &entity.Edges[j]
			fmt.Fprintf(&dump, "  edge %s target=%s card=%s join=%v traversable=%t through=%v\n",
				edge.Name, edge.Target, edge.Cardinality, edge.Join, edge.Traversable, edge.Through,
			)
		}
	}

	sum := sha256.Sum256([]byte(dump.String()))
	return "sha256:" + hex.EncodeToString(sum[:])
}

func EmitSource(catalog *reportcatalog.Catalog, version string) ([]byte, error) {
	var out strings.Builder

	out.WriteString("// Code generated by reportcataloggen. DO NOT EDIT.\n")
	out.WriteString("//\n")
	out.WriteString("// The reporting field catalog and join graph, generated from domain bun tags\n")
	out.WriteString("// and the reportcatalog.yml curation manifest.\n\n")
	out.WriteString("package reportcatalog\n\n")
	out.WriteString("import (\n")
	out.WriteString("\t\"github.com/emoss08/trenova/internal/core/domain/permission\"\n")
	out.WriteString("\t\"github.com/emoss08/trenova/pkg/buncolgen\"\n")
	out.WriteString(")\n\n")

	fmt.Fprintf(&out, "const Version = %q\n\n", version)

	out.WriteString("var Default = Catalog{\n")
	out.WriteString("\tVersion: Version,\n")
	out.WriteString("\tEntities: []Entity{\n")

	for i := range catalog.Entities {
		emitEntity(&out, &catalog.Entities[i])
	}

	out.WriteString("\t},\n")
	out.WriteString("}\n\n")
	out.WriteString("func init() { Default.index() }\n")

	formatted, err := format.Source([]byte(out.String()))
	if err != nil {
		return nil, fmt.Errorf("formatting generated catalog: %w", err)
	}

	return formatted, nil
}

func emitEntity(out *strings.Builder, entity *reportcatalog.Entity) {
	out.WriteString("\t\t{\n")
	fmt.Fprintf(out, "\t\t\tKey:      %q,\n", entity.Key)
	fmt.Fprintf(out, "\t\t\tResource: permission.Resource(%q),\n", entity.Resource)
	fmt.Fprintf(out, "\t\t\tTable: buncolgen.TableInfo{Name: %q, Alias: %q, PrimaryKey: %s},\n",
		entity.Table.Name, entity.Table.Alias, stringSliceLiteral(entity.Table.PrimaryKey))
	fmt.Fprintf(out, "\t\t\tLabel:       %q,\n", entity.Label)
	fmt.Fprintf(out, "\t\t\tPluralLabel: %q,\n", entity.PluralLabel)
	if entity.Description != "" {
		fmt.Fprintf(out, "\t\t\tDescription: %q,\n", entity.Description)
	}
	fmt.Fprintf(out, "\t\t\tCategory: %q,\n", entity.Category)
	if entity.Tenant.IsTenanted() {
		fmt.Fprintf(out, "\t\t\tTenant: TenantColumns{OrganizationID: %q, BusinessUnitID: %q},\n",
			entity.Tenant.OrganizationID, entity.Tenant.BusinessUnitID)
	}
	if entity.OwnershipColumn != "" {
		fmt.Fprintf(out, "\t\t\tOwnershipColumn: %q,\n", entity.OwnershipColumn)
	}

	out.WriteString("\t\t\tFields: []Field{\n")
	for i := range entity.Fields {
		emitField(out, &entity.Fields[i])
	}
	out.WriteString("\t\t\t},\n")

	if len(entity.Edges) > 0 {
		out.WriteString("\t\t\tEdges: []Edge{\n")
		for i := range entity.Edges {
			emitEdge(out, &entity.Edges[i])
		}
		out.WriteString("\t\t\t},\n")
	}

	out.WriteString("\t\t},\n")
}

func emitField(out *strings.Builder, field *reportcatalog.Field) {
	out.WriteString("\t\t\t\t{\n")
	fmt.Fprintf(out, "\t\t\t\t\tKey:    %q,\n", field.Key)
	fmt.Fprintf(out, "\t\t\t\t\tColumn: buncolgen.NewColumn(%q, %q),\n", field.Column.Name, field.Column.Alias)
	fmt.Fprintf(out, "\t\t\t\t\tLabel:  %q,\n", field.Label)
	if field.Description != "" {
		fmt.Fprintf(out, "\t\t\t\t\tDescription: %q,\n", field.Description)
	}
	fmt.Fprintf(out, "\t\t\t\t\tType: %s,\n", fieldTypeConsts[field.Type])
	if field.Format != reportcatalog.FormatNone {
		fmt.Fprintf(out, "\t\t\t\t\tFormat: %s,\n", formatConsts[field.Format])
	}
	if field.Nullable {
		out.WriteString("\t\t\t\t\tNullable: true,\n")
	}
	if len(field.EnumValues) > 0 {
		out.WriteString("\t\t\t\t\tEnumValues: []EnumValue{\n")
		for _, ev := range field.EnumValues {
			fmt.Fprintf(out, "\t\t\t\t\t\t{Value: %q, Label: %q},\n", ev.Value, ev.Label)
		}
		out.WriteString("\t\t\t\t\t},\n")
	}
	if len(field.Aggregations) > 0 {
		names := make([]string, 0, len(field.Aggregations))
		for _, agg := range field.Aggregations {
			names = append(names, aggregationConsts[agg])
		}
		fmt.Fprintf(out, "\t\t\t\t\tAggregations: []Aggregation{%s},\n", strings.Join(names, ", "))
	}
	if field.Filterable {
		out.WriteString("\t\t\t\t\tFilterable: true,\n")
	}
	if field.Groupable {
		out.WriteString("\t\t\t\t\tGroupable: true,\n")
	}
	out.WriteString("\t\t\t\t},\n")
}

func emitEdge(out *strings.Builder, edge *reportcatalog.Edge) {
	out.WriteString("\t\t\t\t{\n")
	fmt.Fprintf(out, "\t\t\t\t\tName:        %q,\n", edge.Name)
	fmt.Fprintf(out, "\t\t\t\t\tLabel:       %q,\n", edge.Label)
	fmt.Fprintf(out, "\t\t\t\t\tSource:      %q,\n", edge.Source)
	fmt.Fprintf(out, "\t\t\t\t\tTarget:      %q,\n", edge.Target)
	fmt.Fprintf(out, "\t\t\t\t\tCardinality: %s,\n", cardinalityConsts[edge.Cardinality])
	if len(edge.Join) > 0 {
		out.WriteString("\t\t\t\t\tJoin: []JoinPair{\n")
		for _, jp := range edge.Join {
			fmt.Fprintf(out, "\t\t\t\t\t\t{Local: %q, Remote: %q},\n", jp.Local, jp.Remote)
		}
		out.WriteString("\t\t\t\t\t},\n")
	}
	if edge.Through != nil {
		out.WriteString("\t\t\t\t\tThrough: &ThroughJoin{\n")
		fmt.Fprintf(out, "\t\t\t\t\t\tTable: buncolgen.TableInfo{Name: %q, Alias: %q, PrimaryKey: %s},\n",
			edge.Through.Table.Name, edge.Through.Table.Alias, stringSliceLiteral(edge.Through.Table.PrimaryKey))
		out.WriteString("\t\t\t\t\t\tSourceJoin: []JoinPair{\n")
		for _, jp := range edge.Through.SourceJoin {
			fmt.Fprintf(out, "\t\t\t\t\t\t\t{Local: %q, Remote: %q},\n", jp.Local, jp.Remote)
		}
		out.WriteString("\t\t\t\t\t\t},\n")
		out.WriteString("\t\t\t\t\t\tTargetJoin: []JoinPair{\n")
		for _, jp := range edge.Through.TargetJoin {
			fmt.Fprintf(out, "\t\t\t\t\t\t\t{Local: %q, Remote: %q},\n", jp.Local, jp.Remote)
		}
		out.WriteString("\t\t\t\t\t\t},\n")
		if edge.Through.Tenant.IsTenanted() {
			fmt.Fprintf(out, "\t\t\t\t\t\tTenant: TenantColumns{OrganizationID: %q, BusinessUnitID: %q},\n",
				edge.Through.Tenant.OrganizationID, edge.Through.Tenant.BusinessUnitID)
		}
		out.WriteString("\t\t\t\t\t},\n")
	}
	if edge.Traversable {
		out.WriteString("\t\t\t\t\tTraversable: true,\n")
	}
	out.WriteString("\t\t\t\t},\n")
}

func stringSliceLiteral(values []string) string {
	if len(values) == 0 {
		return "nil"
	}
	quoted := make([]string, 0, len(values))
	for _, v := range values {
		quoted = append(quoted, fmt.Sprintf("%q", v))
	}
	return "[]string{" + strings.Join(quoted, ", ") + "}"
}
