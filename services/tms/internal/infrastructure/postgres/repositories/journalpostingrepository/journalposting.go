package journalpostingrepository

import (
	"context"
	"fmt"

	"github.com/emoss08/trenova/internal/core/ports/repositories"
	"github.com/emoss08/trenova/internal/infrastructure/postgres"
	"github.com/uptrace/bun"
	"go.uber.org/fx"
	"go.uber.org/zap"
)

type Params struct {
	fx.In

	DB     *postgres.Connection
	Logger *zap.Logger
}

type repository struct {
	db *postgres.Connection
	l  *zap.Logger
}

func New(p Params) repositories.JournalPostingRepository {
	return &repository{db: p.DB, l: p.Logger.Named("postgres.journal-posting-repository")}
}

type journalBatchRecord struct {
	bun.BaseModel `bun:"table:journal_batches"`

	ID             string `bun:"id,pk"`
	OrganizationID string `bun:"organization_id,pk"`
	BusinessUnitID string `bun:"business_unit_id,pk"`
	BatchNumber    string `bun:"batch_number"`
	BatchType      string `bun:"batch_type"`
	Status         string `bun:"status"`
	Description    string `bun:"description"`
	AccountingDate int64  `bun:"accounting_date"`
	FiscalYearID   string `bun:"fiscal_year_id"`
	FiscalPeriodID string `bun:"fiscal_period_id"`
	EntryCount     int    `bun:"entry_count"`
	PostedAt       *int64 `bun:"posted_at,nullzero"`
	PostedByID     string `bun:"posted_by_id,nullzero"`
	CreatedByID    string `bun:"created_by_id"`
	UpdatedByID    string `bun:"updated_by_id"`
}

type journalEntryRecord struct {
	bun.BaseModel `bun:"table:journal_entries"`

	ID               string `bun:"id,pk"`
	BusinessUnitID   string `bun:"business_unit_id,pk"`
	OrganizationID   string `bun:"organization_id,pk"`
	BatchID          string `bun:"batch_id"`
	FiscalYearID     string `bun:"fiscal_year_id"`
	FiscalPeriodID   string `bun:"fiscal_period_id"`
	EntryNumber      string `bun:"entry_number"`
	EntryDate        int64  `bun:"entry_date"`
	EntryType        string `bun:"entry_type"`
	AccountingDate   int64  `bun:"accounting_date"`
	Status           string `bun:"status"`
	ReferenceNumber  string `bun:"reference_number"`
	ReferenceType    string `bun:"reference_type"`
	ReferenceID      string `bun:"reference_id"`
	Description      string `bun:"description"`
	TotalDebit       int64  `bun:"total_debit"`
	TotalCredit      int64  `bun:"total_credit"`
	IsPosted         bool   `bun:"is_posted"`
	PostedAt         *int64 `bun:"posted_at,nullzero"`
	PostedByID       string `bun:"posted_by_id,nullzero"`
	IsAutoGenerated  bool   `bun:"is_auto_generated"`
	RequiresApproval bool   `bun:"requires_approval"`
	IsApproved       bool   `bun:"is_approved"`
	ApprovedAt       *int64 `bun:"approved_at,nullzero"`
	ApprovedByID     string `bun:"approved_by_id,nullzero"`
	CreatedByID      string `bun:"created_by_id"`
	UpdatedByID      string `bun:"updated_by_id"`
}

type journalEntryLineRecord struct {
	bun.BaseModel `bun:"table:journal_entry_lines"`

	ID             string `bun:"id,pk"`
	BusinessUnitID string `bun:"business_unit_id,pk"`
	OrganizationID string `bun:"organization_id,pk"`
	JournalEntryID string `bun:"journal_entry_id"`
	GLAccountID    string `bun:"gl_account_id"`
	LineNumber     int16  `bun:"line_number"`
	Description    string `bun:"description"`
	DebitAmount    int64  `bun:"debit_amount"`
	CreditAmount   int64  `bun:"credit_amount"`
	NetAmount      int64  `bun:"net_amount"`
	CustomerID     string `bun:"customer_id,nullzero"`
	LocationID     string `bun:"location_id,nullzero"`
}

func (r *repository) CreatePosting(
	ctx context.Context,
	params repositories.CreateJournalPostingParams,
) error {
	batch := &journalBatchRecord{
		ID:             params.BatchID.String(),
		OrganizationID: params.OrganizationID.String(),
		BusinessUnitID: params.BusinessUnitID.String(),
		BatchNumber:    params.BatchNumber,
		BatchType:      params.BatchType,
		Status:         params.BatchStatus,
		Description:    params.BatchDescription,
		AccountingDate: params.AccountingDate,
		FiscalYearID:   params.FiscalYearID.String(),
		FiscalPeriodID: params.FiscalPeriodID.String(),
		EntryCount:     1,
		PostedAt:       params.PostedAt,
		PostedByID:     params.PostedByID.String(),
		CreatedByID:    params.CreatedByID.String(),
		UpdatedByID:    params.UpdatedByID.String(),
	}

	entry := &journalEntryRecord{
		ID:               params.EntryID.String(),
		BusinessUnitID:   params.BusinessUnitID.String(),
		OrganizationID:   params.OrganizationID.String(),
		BatchID:          params.BatchID.String(),
		FiscalYearID:     params.FiscalYearID.String(),
		FiscalPeriodID:   params.FiscalPeriodID.String(),
		EntryNumber:      params.EntryNumber,
		EntryDate:        params.AccountingDate,
		EntryType:        params.EntryType,
		AccountingDate:   params.AccountingDate,
		Status:           params.EntryStatus,
		ReferenceNumber:  params.ReferenceNumber,
		ReferenceType:    params.ReferenceType,
		ReferenceID:      params.ReferenceID,
		Description:      params.EntryDescription,
		TotalDebit:       params.TotalDebit,
		TotalCredit:      params.TotalCredit,
		IsPosted:         params.IsPosted,
		PostedAt:         params.PostedAt,
		PostedByID:       params.PostedByID.String(),
		IsAutoGenerated:  params.IsAutoGenerated,
		RequiresApproval: params.RequiresApproval,
		IsApproved:       params.IsApproved,
		ApprovedAt:       params.ApprovedAt,
		ApprovedByID:     params.ApprovedByID.String(),
		CreatedByID:      params.CreatedByID.String(),
		UpdatedByID:      params.UpdatedByID.String(),
	}

	lines := make([]*journalEntryLineRecord, 0, len(params.Lines))
	for _, line := range params.Lines {
		lines = append(lines, &journalEntryLineRecord{
			ID:             line.ID.String(),
			BusinessUnitID: params.BusinessUnitID.String(),
			OrganizationID: params.OrganizationID.String(),
			JournalEntryID: params.EntryID.String(),
			GLAccountID:    line.GLAccountID.String(),
			LineNumber:     line.LineNumber,
			Description:    line.Description,
			DebitAmount:    line.DebitAmount,
			CreditAmount:   line.CreditAmount,
			NetAmount:      line.NetAmount,
			CustomerID:     line.CustomerID.String(),
			LocationID:     line.LocationID.String(),
		})
	}

	if _, err := r.db.DBForContext(ctx).NewInsert().Model(batch).Exec(ctx); err != nil {
		return fmt.Errorf("insert journal batch: %w", err)
	}
	if _, err := r.db.DBForContext(ctx).NewInsert().Model(entry).Exec(ctx); err != nil {
		return fmt.Errorf("insert journal entry: %w", err)
	}
	if len(lines) > 0 {
		if _, err := r.db.DBForContext(ctx).NewInsert().Model(&lines).Exec(ctx); err != nil {
			return fmt.Errorf("insert journal entry lines: %w", err)
		}
	}

	return nil
}
