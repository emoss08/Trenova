type Params struct {
	fx.In

	DB     *postgres.Connection
	Logger *zap.Logger
}

type repository struct {
	db *postgres.Connection
	l  *zap.Logger
}

func New(p Params) repositories.JournalPostingRepository {
	return &repository{db: p.DB, l: p.Logger.Named("postgres.journal-posting-repository")}
}

