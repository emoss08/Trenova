package repositories

import (
	"context"

	"github.com/emoss08/trenova/shared/pulid"
)

type JournalPostingLine struct {
	ID           pulid.ID `json:"id"`
	GLAccountID  pulid.ID `json:"glAccountId"`
	LineNumber   int16    `json:"lineNumber"`
	Description  string   `json:"description"`
	DebitAmount  int64    `json:"debitAmount"`
	CreditAmount int64    `json:"creditAmount"`
	NetAmount    int64    `json:"netAmount"`
	CustomerID   pulid.ID `json:"customerId"`
	LocationID   pulid.ID `json:"locationId"`
}

type CreateJournalPostingParams struct {
	BatchID          pulid.ID `json:"batchId"`
	OrganizationID   pulid.ID `json:"organizationId"`
	BusinessUnitID   pulid.ID `json:"businessUnitId"`
	BatchNumber      string   `json:"batchNumber"`
	BatchType        string   `json:"batchType"`
	BatchStatus      string   `json:"batchStatus"`
	BatchDescription string   `json:"batchDescription"`
	FiscalYearID     pulid.ID `json:"fiscalYearId"`
	FiscalPeriodID   pulid.ID `json:"fiscalPeriodId"`
	AccountingDate   int64    `json:"accountingDate"`
	PostedAt         *int64   `json:"postedAt"`
	PostedByID       pulid.ID `json:"postedById"`
	CreatedByID      pulid.ID `json:"createdById"`
	UpdatedByID      pulid.ID `json:"updatedById"`

	EntryID              pulid.ID             `json:"entryId"`
	EntryNumber          string               `json:"entryNumber"`
	EntryType            string               `json:"entryType"`
	EntryStatus          string               `json:"entryStatus"`
	ReferenceNumber      string               `json:"referenceNumber"`
	ReferenceType        string               `json:"referenceType"`
	ReferenceID          string               `json:"referenceId"`
	EntryDescription     string               `json:"entryDescription"`
	TotalDebit           int64                `json:"totalDebit"`
	TotalCredit          int64                `json:"totalCredit"`
	IsPosted             bool                 `json:"isPosted"`
	IsAutoGenerated      bool                 `json:"isAutoGenerated"`
	IsReversal           bool                 `json:"isReversal"`
	ReversalOfID         pulid.ID             `json:"reversalOfId"`
	ReversalDate         *int64               `json:"reversalDate"`
	ReversalReason       string               `json:"reversalReason"`
	RequiresApproval     bool                 `json:"requiresApproval"`
	IsApproved           bool                 `json:"isApproved"`
	ApprovedByID         pulid.ID             `json:"approvedById"`
	ApprovedAt           *int64               `json:"approvedAt"`
	SourceID             pulid.ID             `json:"sourceId"`
	SourceObjectType     string               `json:"sourceObjectType"`
	SourceObjectID       string               `json:"sourceObjectId"`
	SourceEventType      string               `json:"sourceEventType"`
	SourceStatus         string               `json:"sourceStatus"`
	SourceDocumentNumber string               `json:"sourceDocumentNumber"`
	SourceIdempotencyKey string               `json:"sourceIdempotencyKey"`
	Lines                []JournalPostingLine `json:"lines"`
}

type JournalPostingRepository interface {
	CreatePosting(ctx context.Context, params CreateJournalPostingParams) error
}
