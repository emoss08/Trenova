func routeRefsFor(method string, paths ...string) []RouteRef {
	routes := make([]RouteRef, 0, len(paths))
	for _, path := range paths {
		routes = append(routes, RouteRef{
			Method: method,
			Path:   path,
		})
	}

	return routes
}

func mergeRouteRefs(groups ...[]RouteRef) []RouteRef {
	totalRoutes := 0
	for _, group := range groups {
		totalRoutes += len(group)
	}

	routes := make([]RouteRef, 0, totalRoutes)
	for _, group := range groups {
		routes = append(routes, group...)
	}

	return routes
}
