func (je *JournalEntry) BeforeAppendModel(_ context.Context, query bun.Query) error {
	now := utils.NowUnix()

	switch query.(type) {
	case *bun.InsertQuery:
		if je.ID.IsNil() {
			je.ID = pulid.MustNew("je_")
		}

		je.CreatedAt = now
	case *bun.UpdateQuery:
		je.UpdatedAt = now
	}

	return nil
}

// IsBalanced checks if the journal entry debits equal credits
func (je *JournalEntry) IsBalanced() bool {
	return je.TotalDebit == je.TotalCredit
}

// CanBePosted checks if the journal entry can be posted
func (je *JournalEntry) CanBePosted() bool {
	if je.IsPosted {
		return false
	}
	if je.RequiresApproval && !je.IsApproved {
		return false
	}
	if !je.IsBalanced() {
		return false
	}
	return je.Status == JournalEntryStatusDraft || je.Status == JournalEntryStatusApproved
}

// CanBeReversed checks if the journal entry can be reversed
func (je *JournalEntry) CanBeReversed() bool {
	return je.IsPosted && !je.IsReversal && je.ReversedByID == nil
}
