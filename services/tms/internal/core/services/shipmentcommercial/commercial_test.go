func TestRecalculate_PreservesUserEditedDetentionChargeAmount(t *testing.T) {
	t.Parallel()

	detentionChargeID := pulid.MustNew("acc_")
	entity := validShipment()
	arrival := int64(100)
	entity.Moves[0].Stops[0].ActualArrival = &arrival
	entity.AdditionalCharges = []*shipment.AdditionalCharge{
		{
			ID:                  pulid.MustNew("ac_"),
			AccessorialChargeID: detentionChargeID,
			Method:              accessorialcharge.MethodFlat,
			Amount:              decimal.NewFromInt(502),
			Unit:                1,
		},
	}

	control := &tenant.ShipmentControl{
		TrackDetentionTime:           true,
		AutoGenerateDetentionCharges: true,
		DetentionThreshold:           int16Ptr(30),
		DetentionChargeID:            &detentionChargeID,
	}

	formula := mocks.NewMockFormulaCalculator(t)
	formula.EXPECT().
		Calculate(mock.Anything, mock.AnythingOfType("*formulatemplatetypes.CalculateRequest")).
		Return(&formulatemplatetypes.CalculateResponse{Amount: decimal.NewFromInt(100)}, nil).
		Once()

	accessorialRepo := mocks.NewMockAccessorialChargeRepository(t)
	accessorialRepo.EXPECT().
		GetByID(mock.Anything, mock.Anything).
		Return(&accessorialcharge.AccessorialCharge{
			ID:       detentionChargeID,
			Method:   accessorialcharge.MethodPerUnit,
			RateUnit: accessorialcharge.RateUnitHour,
			Amount:   decimal.NewFromInt(50),
		}, nil).
		Once()

	calculator := New(Params{
		Formula:         formula,
		AccessorialRepo: accessorialRepo,
	})
	calculator.now = func() int64 { return 7300 }

	err := calculator.Recalculate(t.Context(), entity, control, pulid.MustNew("usr_"))

	require.NoError(t, err)
	require.Len(t, entity.AdditionalCharges, 2)
	assert.True(t, decimal.NewFromInt(502).Equal(entity.AdditionalCharges[0].Amount),
		"user-edited amount should be preserved, got %s", entity.AdditionalCharges[0].Amount)
	assert.Equal(t, accessorialcharge.MethodFlat, entity.AdditionalCharges[0].Method,
		"user-edited method should be preserved")
	assert.Equal(t, int16(1), entity.AdditionalCharges[0].Unit,
		"user-edited unit should be preserved")
	assert.True(t, entity.AdditionalCharges[1].IsSystemGenerated)
	assert.Equal(t, accessorialcharge.MethodPerUnit, entity.AdditionalCharges[1].Method)
	assert.True(t, decimal.NewFromInt(50).Equal(entity.AdditionalCharges[1].Amount))
	assert.Equal(t, int16(2), entity.AdditionalCharges[1].Unit)
}

