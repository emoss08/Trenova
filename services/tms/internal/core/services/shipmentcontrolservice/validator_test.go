package shipmentcontrolservice

import (
	"context"
	"testing"

	"github.com/emoss08/trenova/internal/core/domain/tenant"
	"github.com/emoss08/trenova/pkg/errortypes"
	"github.com/emoss08/trenova/pkg/validationframework"
	"github.com/emoss08/trenova/shared/pulid"
	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
)

type testReferenceChecker struct {
	valid bool
}

func (c *testReferenceChecker) CheckReference(
	_ context.Context,
	_ *validationframework.ReferenceRequest,
) (bool, error) {
	return c.valid, nil
}

func TestValidateUpdate_AllowsValidDetentionChargeReference(t *testing.T) {
	t.Parallel()

	v := &Validator{
		validator: validationframework.
			NewTenantedValidatorBuilder[*tenant.ShipmentControl]().
			WithModelName("ShipmentControl").
			WithReferenceChecker(&testReferenceChecker{valid: true}).
			WithOptionalReferenceCheck(
				"detentionChargeId",
				"accessorial_charges",
				"Detention charge does not exist in your organization",
				func(sc *tenant.ShipmentControl) pulid.ID {
					if sc.DetentionChargeID == nil {
						return ""
					}

					return *sc.DetentionChargeID
				},
			).
			Build(),
	}

	entity := validShipmentControl()
	entity.ID = pulid.MustNew("sc_")
	entity.Version = 1
	entity.TrackDetentionTime = true
	entity.AutoGenerateDetentionCharges = true
	detentionChargeID := pulid.MustNew("acc_")
	entity.DetentionChargeID = &detentionChargeID

	multiErr := v.ValidateUpdate(t.Context(), entity)

	assert.Nil(t, multiErr)
}

func TestValidateUpdate_RejectsUnknownDetentionChargeReference(t *testing.T) {
	t.Parallel()

	v := &Validator{
		validator: validationframework.
			NewTenantedValidatorBuilder[*tenant.ShipmentControl]().
			WithModelName("ShipmentControl").
			WithReferenceChecker(&testReferenceChecker{valid: false}).
			WithOptionalReferenceCheck(
				"detentionChargeId",
				"accessorial_charges",
				"Detention charge does not exist in your organization",
				func(sc *tenant.ShipmentControl) pulid.ID {
					if sc.DetentionChargeID == nil {
						return ""
					}

					return *sc.DetentionChargeID
				},
			).
			Build(),
	}

	entity := validShipmentControl()
	entity.ID = pulid.MustNew("sc_")
	entity.Version = 1
	entity.TrackDetentionTime = true
	entity.AutoGenerateDetentionCharges = true
	detentionChargeID := pulid.MustNew("acc_")
	entity.DetentionChargeID = &detentionChargeID

	multiErr := v.ValidateUpdate(t.Context(), entity)

	require.NotNil(t, multiErr)
	assert.True(t, multiErr.HasErrors())
	assertErrorField(t, multiErr, "detentionChargeId")
}

func validShipmentControl() *tenant.ShipmentControl {
	return &tenant.ShipmentControl{
		BusinessUnitID:              pulid.MustNew("bu_"),
		OrganizationID:              pulid.MustNew("org_"),
		MaxShipmentWeightLimit:      80000,
		AutoDelayShipments:          true,
		AutoDelayShipmentsThreshold: int16Ptr(30),
		DetentionThreshold:          int16Ptr(30),
		AutoCancelShipments:         false,
		AllowMoveRemovals:           true,
	}
}

//go:fix inline
func int16Ptr(value int16) *int16 {
	return new(value)
}

func assertErrorField(t *testing.T, multiErr *errortypes.MultiError, field string) {
	t.Helper()

	for _, err := range multiErr.Errors {
		if err.Field == field {
			return
		}
	}

	t.Fatalf("expected validation error for field %q, got %#v", field, multiErr.Errors)
}
