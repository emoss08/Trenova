package shipmentservice

import (
	"context"
	"fmt"

	"github.com/emoss08/trenova/internal/core/domain/shipment"
	"github.com/emoss08/trenova/internal/core/domain/tenant"
	"github.com/emoss08/trenova/internal/core/ports/repositories"
	"github.com/emoss08/trenova/pkg/errortypes"
	"github.com/emoss08/trenova/pkg/validationframework"
	"github.com/emoss08/trenova/shared/pulid"
)

type additionalChargeRuleContext struct {
	index         int
	charge        *shipment.AdditionalCharge
	shipmentID    pulid.ID
	isCreate      bool
	seenChargeIDs map[pulid.ID]string
	multiErr      *errortypes.MultiError
}

func createAdditionalChargeValidationRule(
	controlRepo repositories.ShipmentControlRepository,
) validationframework.TenantedRule[*shipment.Shipment] {
	return validationframework.
		NewTenantedRule[*shipment.Shipment]("shipment_additional_charge_validation").
		OnBoth().
		WithStage(validationframework.ValidationStageDataIntegrity).
		WithPriority(validationframework.ValidationPriorityHigh).
		WithValidation(func(
			ctx context.Context,
			entity *shipment.Shipment,
			valCtx *validationframework.TenantedValidationContext,
			multiErr *errortypes.MultiError,
		) error {
			control, err := loadShipmentControlForValidation(ctx, controlRepo, entity)
			if err != nil {
				multiErr.Add(
					"additionalCharges",
					errortypes.ErrInvalid,
					"Unable to load shipment control policy",
				)
				return nil
			}

			seenChargeIDs := make(map[pulid.ID]string, len(entity.AdditionalCharges))

			for index, charge := range entity.AdditionalCharges {
				if charge == nil {
					continue
				}

				charge.Validate(multiErr.WithIndex("additionalCharges", index))

				ruleCtx := additionalChargeRuleContext{
					index:         index,
					charge:        charge,
					shipmentID:    entity.ID,
					isCreate:      valCtx.IsCreate(),
					seenChargeIDs: seenChargeIDs,
					multiErr:      multiErr,
				}

				validateAdditionalChargeIdentifiers(ruleCtx)
				validateDetentionChargeManagement(ruleCtx, control)
			}

			return nil
		})
}

func validateAdditionalChargeIdentifiers(ctx additionalChargeRuleContext) {
	if ctx.isCreate {
		if !ctx.charge.ID.IsNil() {
			ctx.multiErr.Add(
				additionalChargeFieldPath(ctx.index, "id"),
				errortypes.ErrInvalid,
				"Additional charge ID must not be provided when creating a shipment",
			)
		}
		if !ctx.charge.ShipmentID.IsNil() {
			ctx.multiErr.Add(
				additionalChargeFieldPath(ctx.index, "shipmentId"),
				errortypes.ErrInvalid,
				"Additional charge shipment ID must not be provided when creating a shipment",
			)
		}
	} else if !ctx.shipmentID.IsNil() &&
		!ctx.charge.ShipmentID.IsNil() &&
		ctx.charge.ShipmentID != ctx.shipmentID {
		ctx.multiErr.Add(
			additionalChargeFieldPath(ctx.index, "shipmentId"),
			errortypes.ErrInvalid,
			"Additional charge shipment ID must match the shipment being updated",
		)
	}

	if ctx.charge.ID.IsNil() {
		return
	}

	currentPath := additionalChargeFieldPath(ctx.index, "id")
	if firstPath, ok := ctx.seenChargeIDs[ctx.charge.ID]; ok {
		ctx.multiErr.Add(
			currentPath,
			errortypes.ErrDuplicate,
			fmt.Sprintf("Additional charge ID duplicates %s", firstPath),
		)
		return
	}

	ctx.seenChargeIDs[ctx.charge.ID] = currentPath
}

func validateDetentionChargeManagement(
	ctx additionalChargeRuleContext,
	control *tenant.ShipmentControl,
) {
	if control == nil || !control.AutoGenerateDetentionCharges {
		return
	}

	detentionChargeID := control.DetentionChargeID
	if detentionChargeID == nil || detentionChargeID.IsNil() {
		return
	}

	if ctx.charge.AccessorialChargeID != *detentionChargeID || ctx.charge.IsSystemGenerated {
		return
	}

	ctx.multiErr.Add(
		additionalChargeFieldPath(ctx.index, "accessorialChargeId"),
		errortypes.ErrInvalidOperation,
		"Manual detention charges cannot be added when auto-generate detention charges is enabled",
	)
}

func additionalChargeFieldPath(index int, field string) string {
	return fmt.Sprintf("additionalCharges[%d].%s", index, field)
}
