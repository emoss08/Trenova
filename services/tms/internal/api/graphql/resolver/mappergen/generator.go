const generatedHeader = "// Code generated by resolver/mappergen; DO NOT EDIT.\n\n"

func run(opts *generatorOptions) error {
	cwd, err := os.Getwd()
	if err != nil {
		return fmt.Errorf("resolving current directory: %w", err)
	}

	resolverDir := valueOrDefault(opts.ResolverDir, cwd)
	moduleRoot, err := findModuleRoot(resolverDir)
	if err != nil {
		return err
	}

	fillDefaults(opts, moduleRoot, resolverDir)

	modulePath, err := loadModulePath(opts.GoModPath)
	if err != nil {
		return err
	}
	cfg, err := loadGqlgenConfig(opts.GqlgenPath)
	if err != nil {
		return err
	}
	manifest, err := loadManifest(opts.ManifestPath)
	if err != nil {
		return err
	}
	inputs, err := loadStructsFromFile(opts.ModelPath)
	if err != nil {
		return err
	}
	existing, err := loadExistingFunctions(opts.ResolverDir)
	if err != nil {
		return err
	}

	generated := make([]generatedType, 0, len(cfg.Models))
	for _, binding := range domainBindings(cfg, modulePath) {
		item, ok, buildErr := buildGeneratedType(
			binding,
			manifest,
			inputs.Structs,
			existing,
			opts.DomainDir,
			modulePath,
		)
		if buildErr != nil {
			return buildErr
		}
		if ok {
			generated = append(generated, item)
		}
	}

	sort.Slice(generated, func(i, j int) bool {
		return generated[i].Name < generated[j].Name
	})

	if opts.OutputPath != "" {
		output, renderErr := renderGenerated(generated, modulePath)
		if renderErr != nil {
			return renderErr
		}
		if err = os.WriteFile(opts.OutputPath, output, 0o600); err != nil {
			return fmt.Errorf("writing generated mappers %q: %w", opts.OutputPath, err)
		}
		return nil
	}

	return writeGeneratedPackage(opts.OutputDir, generated, modulePath)
}

func fillDefaults(opts *generatorOptions, moduleRoot, resolverDir string) {
	opts.ResolverDir = valueOrDefault(opts.ResolverDir, resolverDir)
	opts.ManifestPath = valueOrDefault(opts.ManifestPath, filepath.Join(resolverDir, "mappers.yml"))
	opts.OutputDir = valueOrDefault(opts.OutputDir, filepath.Join(resolverDir, "mappers"))
	opts.GqlgenPath = valueOrDefault(opts.GqlgenPath, filepath.Join(moduleRoot, "gqlgen.yml"))
	opts.ModelPath = valueOrDefault(
		opts.ModelPath,
		filepath.Join(moduleRoot, "internal", "api", "graphql", "gqlmodel", "models_gen.go"),
	)
	opts.DomainDir = valueOrDefault(
		opts.DomainDir,
		filepath.Join(moduleRoot, "internal", "core", "domain"),
	)
	opts.GoModPath = valueOrDefault(opts.GoModPath, filepath.Join(moduleRoot, "go.mod"))
}

func valueOrDefault(value, fallback string) string {
	if value != "" {
		return value
	}
	return fallback
}

func findModuleRoot(start string) (string, error) {
	current, err := filepath.Abs(start)
	if err != nil {
		return "", fmt.Errorf("resolving %q: %w", start, err)
	}

	for {
		if _, statErr := os.Stat(filepath.Join(current, "go.mod")); statErr == nil {
			return current, nil
		}
		parent := filepath.Dir(current)
		if parent == current {
			return "", fmt.Errorf("could not find go.mod from %q", start)
		}
		current = parent
	}
}

func loadModulePath(path string) (string, error) {
	input, err := os.ReadFile(path)
	if err != nil {
		return "", fmt.Errorf("reading go.mod %q: %w", path, err)
	}
	modulePath := modfile.ModulePath(input)
	if modulePath == "" {
		return "", fmt.Errorf("go.mod %q does not declare a module path", path)
	}

	return modulePath, nil
}

func loadManifest(path string) (manifest, error) {
	input, err := os.ReadFile(path)
	if err != nil {
		if os.IsNotExist(err) {
			return manifest{Types: map[string]typeOverride{}}, nil
		}
		return manifest{}, fmt.Errorf("reading manifest %q: %w", path, err)
	}
	if len(bytes.TrimSpace(input)) == 0 {
		return manifest{Types: map[string]typeOverride{}}, nil
	}

	var data manifest
	decoder := yaml.NewDecoder(bytes.NewReader(input))
	decoder.KnownFields(true)
	if err = decoder.Decode(&data); err != nil {
		return manifest{}, fmt.Errorf("decoding manifest %q: %w", path, err)
	}
	if data.Types == nil {
		data.Types = map[string]typeOverride{}
	}

	return data, nil
}

func loadGqlgenConfig(path string) (gqlgenConfig, error) {
	input, err := os.ReadFile(path)
	if err != nil {
		return gqlgenConfig{}, fmt.Errorf("reading gqlgen config %q: %w", path, err)
	}

	var cfg gqlgenConfig
	if err = yaml.Unmarshal(input, &cfg); err != nil {
		return gqlgenConfig{}, fmt.Errorf("decoding gqlgen config %q: %w", path, err)
	}

	return cfg, nil
}

func loadStructsFromFile(path string) (parsedPackage, error) {
	fset := token.NewFileSet()
	file, err := parser.ParseFile(fset, path, nil, parser.ParseComments)
	if err != nil {
		return parsedPackage{}, fmt.Errorf("parsing Go file %q: %w", path, err)
	}

	pkg := parsedPackage{
		Name:    file.Name.Name,
		Files:   []*ast.File{file},
		Structs: map[string]goStruct{},
	}
	collectStructs(&pkg)

	return pkg, nil
}

func loadStructsFromDir(path string) (parsedPackage, error) {
	fset := token.NewFileSet()
	pkgs, err := parser.ParseDir(
		fset,
		path,
		func(info os.FileInfo) bool {
			name := info.Name()
			return !strings.HasSuffix(name, "_test.go") && strings.HasSuffix(name, ".go")
		},
		0,
	)
	if err != nil {
		return parsedPackage{}, fmt.Errorf("parsing Go package %q: %w", path, err)
	}
	if len(pkgs) == 0 {
		return parsedPackage{}, fmt.Errorf("no Go package found in %q", path)
	}

	names := make([]string, 0, len(pkgs))
	for name := range pkgs {
		names = append(names, name)
	}
	sort.Strings(names)
	pkg := parsedPackage{
		Name:    names[0],
		Files:   make([]*ast.File, 0, len(pkgs[names[0]].Files)),
		Structs: map[string]goStruct{},
	}
	for _, file := range pkgs[names[0]].Files {
		pkg.Files = append(pkg.Files, file)
	}
	collectStructs(&pkg)

	return pkg, nil
}

func collectStructs(pkg *parsedPackage) {
	for _, file := range pkg.Files {
		for _, decl := range file.Decls {
			genDecl, isGenDecl := decl.(*ast.GenDecl)
			if !isGenDecl || genDecl.Tok != token.TYPE {
				continue
			}
			for _, spec := range genDecl.Specs {
				typeSpec, isTypeSpec := spec.(*ast.TypeSpec)
				if !isTypeSpec {
					continue
				}
				structType, isStructType := typeSpec.Type.(*ast.StructType)
				if !isStructType {
					continue
				}
				pkg.Structs[typeSpec.Name.Name] = goStruct{
					PackageName: pkg.Name,
					Name:        typeSpec.Name.Name,
					Fields:      collectFields(structType),
				}
			}
		}
	}
}

func collectFields(structType *ast.StructType) map[string]goField {
	fields := map[string]goField{}
	for _, field := range structType.Fields.List {
		if len(field.Names) == 0 {
			continue
		}
		jsonName := jsonFieldName(field)
		if jsonName == "-" {
			continue
		}
		ref := exprTypeRef(field.Type)
		for _, name := range field.Names {
			goName := name.Name
			key := jsonName
			if key == "" {
				key = lowerFirst(goName)
			}
			fields[key] = goField{
				GoName:   goName,
				JSONName: key,
				Type:     ref,
			}
		}
	}

	return fields
}

func jsonFieldName(field *ast.Field) string {
	if field.Tag == nil {
		return ""
	}
	raw, err := strconv.Unquote(field.Tag.Value)
	if err != nil {
		return ""
	}
	tag := reflect.StructTag(raw).Get("json")
	if tag == "" {
		return ""
	}
	name, _, _ := strings.Cut(tag, ",")
	return name
}

func exprTypeRef(expr ast.Expr) typeRef {
	switch node := expr.(type) {
	case *ast.StarExpr:
		ref := exprTypeRef(node.X)
		ref.Pointer = true
		return ref
	case *ast.Ident:
		return typeRef{Name: node.Name}
	case *ast.SelectorExpr:
		return typeRef{Name: exprString(node)}
	case *ast.MapType:
		return typeRef{Name: exprString(node), Map: true}
	case *ast.ArrayType:
		ref := exprTypeRef(node.Elt)
		ref.Slice = true
		ref.Name = "[]" + ref.Name
		return ref
	case *ast.IndexExpr:
		name := exprString(node.X)
		if name == "graphql.Omittable" {
			inner := exprTypeRef(node.Index)
			return typeRef{
				Name:          name,
				Omittable:     true,
				OmittableType: &inner,
			}
		}
		return typeRef{Name: exprString(node)}
	default:
		return typeRef{Name: exprString(node)}
	}
}

func exprString(expr ast.Expr) string {
	var buf bytes.Buffer
	_ = format.Node(&buf, token.NewFileSet(), expr)
	return buf.String()
}

func loadExistingFunctions(resolverDir string) (map[string]struct{}, error) {
	matches, err := filepath.Glob(filepath.Join(resolverDir, "*.go"))
	if err != nil {
		return nil, fmt.Errorf("globbing resolver files: %w", err)
	}
	sort.Strings(matches)

	existing := map[string]struct{}{}
	fset := token.NewFileSet()
	for _, match := range matches {
		if strings.HasSuffix(filepath.Base(match), "_gen.go") {
			continue
		}
		file, parseErr := parser.ParseFile(fset, match, nil, 0)
		if parseErr != nil {
			return nil, fmt.Errorf("parsing resolver file %q: %w", match, parseErr)
		}
		for _, decl := range file.Decls {
			fn, ok := decl.(*ast.FuncDecl)
			if ok {
				existing[fn.Name.Name] = struct{}{}
			}
		}
	}

	return existing, nil
}

func domainBindings(cfg gqlgenConfig, modulePath string) []modelBinding {
	prefix := modulePath + "/internal/core/domain/"
	bindings := make([]modelBinding, 0, len(cfg.Models))
	for gqlName, model := range cfg.Models {
		if len(model.Model) == 0 {
			continue
		}
		modelPath := model.Model[0]
		if !strings.HasPrefix(modelPath, prefix) {
			continue
		}
		index := strings.LastIndex(modelPath, ".")
		if index < 0 || index == len(modelPath)-1 {
			continue
		}
		importPath := modelPath[:index]
		goName := modelPath[index+1:]
		bindings = append(bindings, modelBinding{
			GraphQLName: gqlName,
			ImportPath:  importPath,
			PackageName: filepath.Base(importPath),
			GoName:      goName,
		})
	}
	sort.Slice(bindings, func(i, j int) bool {
		return bindings[i].GraphQLName < bindings[j].GraphQLName
	})

	return bindings
}

func buildGeneratedType(
	binding modelBinding,
	manifest manifest,
	inputs map[string]goStruct,
	existing map[string]struct{},
	domainDir string,
	modulePath string,
) (generatedType, bool, error) {
	override := manifest.Types[binding.GraphQLName]
	if override.Skip {
		return generatedType{}, false, nil
	}

	createName := lowerFirst(binding.GraphQLName) + "FromInput"
	patchName := "apply" + binding.GraphQLName + "Patch"
	if _, ok := existing[createName]; ok {
		return generatedType{}, false, nil
	}
	if _, ok := existing[patchName]; ok {
		return generatedType{}, false, nil
	}

	inputStruct, hasInput := inputs[binding.GraphQLName+"Input"]
	patchStruct, hasPatch := inputs[binding.GraphQLName+"PatchInput"]
	if !hasInput && !hasPatch {
		return generatedType{}, false, nil
	}

	domainPkg, err := loadDomainPackage(binding, domainDir, modulePath)
	if err != nil {
		return generatedType{}, false, err
	}
	domainStruct, ok := domainPkg.Structs[binding.GoName]
	if !ok {
		return generatedType{}, false, nil
	}
	if !isStandardTenantedEntity(domainStruct) {
		return generatedType{}, false, nil
	}
	binding.PackageName = domainPkg.Name
	domainStruct = qualifyDomainFieldTypes(domainStruct, binding.PackageName)

	item := generatedType{
		Name:        binding.GraphQLName,
		Binding:     binding,
		Imports:     overrideImports(override),
		NeedsDomain: true,
	}
	if hasInput {
		create, needsPulID, createOK := buildCreate(binding, inputStruct, domainStruct, override)
		if !createOK {
			return generatedType{}, false, nil
		}
		item.Create = &create
		item.NeedsPulID = item.NeedsPulID || needsPulID
		item.NeedsAuth = true
	}
	if hasPatch {
		patch, needsPulID, patchOK := buildPatch(binding, patchStruct, domainStruct, override)
		if !patchOK {
			return generatedType{}, false, nil
		}
		item.Patch = &patch
		item.NeedsPulID = item.NeedsPulID || needsPulID
	}
	if item.Create == nil && item.Patch == nil {
		return generatedType{}, false, nil
	}

	return item, true, nil
}

func loadDomainPackage(binding modelBinding, domainDir, modulePath string) (parsedPackage, error) {
	prefix := modulePath + "/internal/core/domain/"
	rel := strings.TrimPrefix(binding.ImportPath, prefix)
	if rel == binding.ImportPath || rel == "" {
		return parsedPackage{}, fmt.Errorf(
			"domain import %q is outside %q",
			binding.ImportPath,
			prefix,
		)
	}

	return loadStructsFromDir(filepath.Join(domainDir, filepath.FromSlash(rel)))
}

func qualifyDomainFieldTypes(domainStruct goStruct, packageName string) goStruct {
	fields := make(map[string]goField, len(domainStruct.Fields))
	for name, field := range domainStruct.Fields {
		field.Type = qualifyDomainTypeRef(field.Type, packageName)
		fields[name] = field
	}
	domainStruct.Fields = fields

	return domainStruct
}

func qualifyDomainTypeRef(ref typeRef, packageName string) typeRef {
	if ref.OmittableType != nil {
		inner := qualifyDomainTypeRef(*ref.OmittableType, packageName)
		ref.OmittableType = &inner
	}
	if ref.Name == "" || isBuiltinType(ref.Name) || strings.Contains(ref.Name, ".") ||
		strings.HasPrefix(ref.Name, "[]") || strings.HasPrefix(ref.Name, "map[") {
		return ref
	}
	ref.Name = packageName + "." + ref.Name

	return ref
}

func isBuiltinType(name string) bool {
	switch name {
	case "any", "bool", "byte", "error", "float32", "float64",
		"int", "int8", "int16", "int32", "int64",
		"rune", "string", "uint", "uint8", "uint16", "uint32", "uint64", "uintptr":
		return true
	default:
		return false
	}
}

func isStandardTenantedEntity(domainStruct goStruct) bool {
	_, hasOrganizationID := domainStruct.Fields["organizationId"]
	_, hasBusinessUnitID := domainStruct.Fields["businessUnitId"]
	return hasOrganizationID && hasBusinessUnitID
}

func buildCreate(
	binding modelBinding,
	inputStruct goStruct,
	domainStruct goStruct,
	override typeOverride,
) (generatedCreate, bool, bool) {
	create := generatedCreate{
		InputName: binding.GraphQLName + "Input",
		Parses:    []parseAssignment{},
		Defaults:  []defaultAssignment{},
		Fields:    []structAssignment{},
	}
	needsPulID := false

	if field, ok := domainStruct.Fields["id"]; ok {
		create.Fields = append(create.Fields, structAssignment{
			FieldName:  field.GoName,
			Expression: "id",
		})
	}
	if field, ok := domainStruct.Fields["organizationId"]; ok {
		create.Fields = append(create.Fields, structAssignment{
			FieldName:  field.GoName,
			Expression: "authCtx.OrganizationID",
		})
	}
	if field, ok := domainStruct.Fields["businessUnitId"]; ok {
		create.Fields = append(create.Fields, structAssignment{
			FieldName:  field.GoName,
			Expression: "authCtx.BusinessUnitID",
		})
	}

	fields := sortedFields(inputStruct.Fields)
	for _, inputField := range fields {
		if shouldExclude(inputField.JSONName, override) {
			continue
		}
		domainJSON := inputField.JSONName
		if override.Aliases != nil && override.Aliases[inputField.JSONName] != "" {
			domainJSON = override.Aliases[inputField.JSONName]
		}
		domainField, ok := domainStruct.Fields[domainJSON]
		if !ok {
			return generatedCreate{}, false, false
		}
		defaultValue, hasDefault := override.Defaults[inputField.JSONName]
		expr, parses, defaultAssign, usesPulID, ok := createExpression(
			inputField,
			domainField,
			defaultValue,
			hasDefault,
		)
		if !ok {
			return generatedCreate{}, false, false
		}
		create.Parses = append(create.Parses, parses...)
		if defaultAssign.VarName != "" {
			create.Defaults = append(create.Defaults, defaultAssign)
		}
		create.Fields = append(create.Fields, structAssignment{
			FieldName:  domainField.GoName,
			Expression: expr,
		})
		needsPulID = needsPulID || usesPulID
	}

	return create, needsPulID, true
}

func buildPatch(
	binding modelBinding,
	patchStruct goStruct,
	domainStruct goStruct,
	override typeOverride,
) (generatedPatch, bool, bool) {
	patch := generatedPatch{
		InputName: binding.GraphQLName + "PatchInput",
		Fields:    []patchAssignment{},
	}
	needsPulID := false

	fields := sortedFields(patchStruct.Fields)
	for _, inputField := range fields {
		if shouldExclude(inputField.JSONName, override) {
			continue
		}
		domainJSON := inputField.JSONName
		if override.Aliases != nil && override.Aliases[inputField.JSONName] != "" {
			domainJSON = override.Aliases[inputField.JSONName]
		}
		domainField, ok := domainStruct.Fields[domainJSON]
		if !ok {
			return generatedPatch{}, false, false
		}
		assignment, usesPulID, ok := patchExpression(inputField, domainField)
		if !ok {
			return generatedPatch{}, false, false
		}
		patch.Fields = append(patch.Fields, assignment)
		needsPulID = needsPulID || usesPulID
	}

	return patch, needsPulID, true
}

func overrideImports(override typeOverride) []generatedImport {
	if len(override.Imports) == 0 {
		return nil
	}

	aliases := make([]string, 0, len(override.Imports))
	for alias := range override.Imports {
		aliases = append(aliases, alias)
	}
	sort.Strings(aliases)

	imports := make([]generatedImport, 0, len(aliases))
	for _, alias := range aliases {
		path := override.Imports[alias]
		importItem := generatedImport{Path: path}
		if alias != filepath.Base(path) {
			importItem.Alias = alias
		}
		imports = append(imports, importItem)
	}

	return imports
}

func sortedFields(fields map[string]goField) []goField {
	values := make([]goField, 0, len(fields))
	for _, field := range fields {
		values = append(values, field)
	}
	sort.Slice(values, func(i, j int) bool {
		return values[i].GoName < values[j].GoName
	})

	return values
}

func shouldExclude(field string, override typeOverride) bool {
	if _, ok := defaultExcludedFields[field]; ok {
		return true
	}
	return slices.Contains(override.Exclude, field)
}

func createExpression(
	inputField,
	domainField goField,
	defaultValue string,
	hasDefault bool,
) (
	expression string,
	parses []parseAssignment,
	defaultAssign defaultAssignment,
	needsPulID,
	ok bool,
) {
	source := "input." + inputField.GoName
	dest := domainField.Type
	src := inputField.Type

	if hasDefault {
		expr, assignment, defaultOK := defaultExpression(inputField, domainField, defaultValue)
		return expr, nil, assignment, false, defaultOK
	}

	if isPulID(dest) {
		varName := lowerFirst(inputField.GoName)
		if before, ok0 := strings.CutSuffix(varName, "ID"); ok0 {
			varName = before + "ID"
		}
		if src.Name == typeNameString && !src.Pointer {
			return varName, []parseAssignment{{
				VarName:    varName,
				Expression: "pulid.MustParse(" + source + ")",
			}}, defaultAssignment{}, true, true
		}
		if src.Name == typeNameString && src.Pointer {
			return varName, []parseAssignment{{
				VarName:    varName,
				Expression: "optionalID(" + source + ")",
			}}, defaultAssignment{}, false, true
		}
		return "", nil, defaultAssignment{}, false, false
	}

	expr, ok := valueExpression(source, src, dest, false)
	return expr, nil, defaultAssignment{}, false, ok
}

func defaultExpression(
	inputField,
	domainField goField,
	defaultValue string,
) (string, defaultAssignment, bool) {
	if !inputField.Type.Pointer {
		return "", defaultAssignment{}, false
	}

	inputValue := "*" + "input." + inputField.GoName
	srcDeref := inputField.Type
	srcDeref.Pointer = false
	overrideValue, ok := valueExpression(inputValue, srcDeref, domainField.Type, true)
	if !ok {
		return "", defaultAssignment{}, false
	}

	varName := lowerFirst(inputField.GoName)
	return varName, defaultAssignment{
		VarName:       varName,
		FieldName:     inputField.GoName,
		DefaultValue:  defaultValue,
		InputOverride: overrideValue,
	}, true
}

func patchExpression(inputField, domainField goField) (patchAssignment, bool, bool) {
	src := inputField.Type
	if src.Omittable {
		valueName := lowerFirst(inputField.GoName) + "Value"
		expr, body, needsPulID, ok := assignmentBody(
			"entity."+domainField.GoName,
			valueName,
			*src.OmittableType,
			domainField.Type,
			true,
		)
		if !ok {
			return patchAssignment{}, false, false
		}
		if len(body) == 0 {
			body = []string{"entity." + domainField.GoName + " = " + expr}
		}
		return patchAssignment{
			FieldName: inputField.GoName,
			Guard:     "valueOK",
			ValueName: valueName,
			Body:      body,
		}, needsPulID, true
	}

	if src.Map || src.Slice {
		expr, ok := valueExpression("input."+inputField.GoName, src, domainField.Type, true)
		if !ok {
			return patchAssignment{}, false, false
		}
		return patchAssignment{
			FieldName: inputField.GoName,
			Guard:     "nil",
			Body:      []string{"entity." + domainField.GoName + " = " + expr},
		}, false, true
	}

	if !src.Pointer {
		return patchAssignment{}, false, false
	}

	expr, body, needsPulID, ok := assignmentBody(
		"entity."+domainField.GoName,
		"input."+inputField.GoName,
		src,
		domainField.Type,
		false,
	)
	if !ok {
		return patchAssignment{}, false, false
	}
	if len(body) == 0 {
		body = []string{"entity." + domainField.GoName + " = " + expr}
	}

	return patchAssignment{
		FieldName: inputField.GoName,
		Guard:     "nil",
		Body:      body,
	}, needsPulID, true
}

func assignmentBody(
	target,
	source string,
	src,
	dest typeRef,
	nullableID bool,
) (expression string, body []string, needsPulID, ok bool) {
	if isPulID(dest) {
		if src.Name != typeNameString {
			return "", nil, false, false
		}
		if src.Pointer {
			idVar := lowerFirst(strings.TrimPrefix(strings.TrimPrefix(target, "entity."), "*"))
			if !nullableID {
				return "", []string{
					idVar + ", err := pulid.MustParse(*" + source + ")",
					"if err != nil {",
					"return err",
					"}",
					target + " = " + idVar,
				}, true, true
			}
			return "", []string{
				idVar + ", err := optionalID(" + source + ")",
				"if err != nil {",
				"return err",
				"}",
				target + " = " + idVar,
			}, false, true
		}
		idVar := lowerFirst(strings.TrimPrefix(strings.TrimPrefix(target, "entity."), "*"))
		return "", []string{
			idVar + ", err := pulid.MustParse(" + source + ")",
			"if err != nil {",
			"return err",
			"}",
			target + " = " + idVar,
		}, true, true
	}

	if nullableID &&
		src.Pointer &&
		src.Name == typeNameString &&
		dest.Name == typeNameString &&
		!dest.Pointer {
		return "StringValue(" + source + ")", nil, false, true
	}

	expr, ok := valueExpression(source, src, dest, true)
	return expr, nil, false, ok
}

func valueExpression(source string, src, dest typeRef, patch bool) (string, bool) {
	if equalTypes(src, dest) {
		return source, true
	}
	if src.Pointer {
		return pointerValueExpression(source, src, dest, patch)
	}
	if src.Name == typeNameInt && dest.Name == typeNameInt64 && !src.Pointer && !dest.Pointer {
		return "int64(" + source + ")", true
	}
	if src.Name == typeNameString && dest.Name == typeNameString && !src.Pointer && !dest.Pointer {
		return source, true
	}

	return "", false
}

func pointerValueExpression(source string, src, dest typeRef, patch bool) (string, bool) {
	if src.Name == typeNameString && dest.Name == typeNameString && !dest.Pointer {
		if patch {
			return "*" + source, true
		}
		return "StringValue(" + source + ")", true
	}
	if src.Name == typeNameInt && dest.Name == typeNameInt64 && !dest.Pointer {
		if patch {
			return "int64(*" + source + ")", true
		}
		return "Int64Value(" + source + ")", true
	}
	if src.Name == typeNameInt && dest.Name == typeNameInt64 && dest.Pointer {
		return "int64Ptr(" + source + ")", true
	}

	srcDeref := src
	srcDeref.Pointer = false
	if equalTypes(srcDeref, dest) {
		return "*" + source, true
	}

	return "", false
}

func equalTypes(left, right typeRef) bool {
	return left.Name == right.Name &&
		left.Pointer == right.Pointer &&
		left.Slice == right.Slice &&
		left.Map == right.Map
}

func isPulID(ref typeRef) bool {
	return ref.Name == "pulid.ID" && !ref.Pointer
}

func writeGeneratedPackage(outputDir string, types []generatedType, modulePath string) error {
	if err := os.MkdirAll(outputDir, 0o755); err != nil {
		return fmt.Errorf("creating mapper output directory %q: %w", outputDir, err)
	}
	if err := removeStaleGeneratedFiles(outputDir); err != nil {
		return err
	}

	helpers, err := renderHelpers(modulePath)
	if err != nil {
		return err
	}
	if err = os.WriteFile(filepath.Join(outputDir, "helpers_gen.go"), helpers, 0o600); err != nil {
		return fmt.Errorf("writing generated mapper helpers: %w", err)
	}

	for i := range types {
		item := &types[i]
		output, renderErr := renderGenerated([]generatedType{*item}, modulePath)
		if renderErr != nil {
			return renderErr
		}
		fileName := camelToSnake(item.Name) + "_mapping_gen.go"
		if writeErr := os.WriteFile(
			filepath.Join(outputDir, fileName),
			output,
			0o600,
		); writeErr != nil {
			return fmt.Errorf("writing generated mapper %q: %w", fileName, writeErr)
		}
	}

	return nil
}

func renderHelpers(modulePath string) ([]byte, error) {
	var buf bytes.Buffer
	buf.WriteString(generatedHeader)
	buf.WriteString("package mappers\n\n")
	buf.WriteString("import " + strconv.Quote(modulePath+"/shared/pulid") + "\n\n")
	buf.WriteString("func optionalID(value *string) (pulid.ID, error) {\n")
	buf.WriteString("if value == nil || *value == \"\" {\n")
	buf.WriteString("return pulid.Nil, nil\n")
	buf.WriteString("}\n")
	buf.WriteString("return pulid.MustParse(*value)\n")
	buf.WriteString("}\n\n")
	buf.WriteString("func StringValue(value *string) string {\n")
	buf.WriteString("if value == nil {\n")
	buf.WriteString("return \"\"\n")
	buf.WriteString("}\n")
	buf.WriteString("return *value\n")
	buf.WriteString("}\n\n")
	buf.WriteString("func int64Ptr(value *int) *int64 {\n")
	buf.WriteString("if value == nil {\n")
	buf.WriteString("return nil\n")
	buf.WriteString("}\n")
	buf.WriteString("converted := int64(*value)\n")
	buf.WriteString("return &converted\n")
	buf.WriteString("}\n\n")
	buf.WriteString("func Int64Value(value *int) int64 {\n")
	buf.WriteString("if value == nil {\n")
	buf.WriteString("return 0\n")
	buf.WriteString("}\n")
	buf.WriteString("return int64(*value)\n")
	buf.WriteString("}\n")

	output, err := format.Source(buf.Bytes())
	if err != nil {
		return nil, fmt.Errorf("formatting generated mapper helpers: %w\n%s", err, buf.String())
	}

	return output, nil
}

func removeStaleGeneratedFiles(outputDir string) error {
	matches, err := filepath.Glob(filepath.Join(outputDir, "*_gen.go"))
	if err != nil {
		return fmt.Errorf("globbing stale generated mappers: %w", err)
	}
	for _, match := range matches {
		input, readErr := os.ReadFile(match)
		if readErr != nil {
			return fmt.Errorf("reading generated mapper %q: %w", match, readErr)
		}
		if !bytes.HasPrefix(input, []byte(generatedHeader)) {
			return fmt.Errorf("refusing to remove non-mappergen file %q", match)
		}
		if err = os.Remove(match); err != nil {
			return fmt.Errorf("removing stale generated mapper %q: %w", match, err)
		}
	}

	return nil
}

func renderGenerated(types []generatedType, modulePath string) ([]byte, error) {
	var buf bytes.Buffer
	buf.WriteString(generatedHeader)
	buf.WriteString("package mappers\n\n")

	imports := generatedImports(types, modulePath)
	if len(imports) > 0 {
		buf.WriteString("import (\n")
		for _, item := range imports {
			if item.Alias != "" {
				buf.WriteString("\t" + item.Alias + " " + strconv.Quote(item.Path) + "\n")
				continue
			}
			buf.WriteString("\t" + strconv.Quote(item.Path) + "\n")
		}
		buf.WriteString(")\n\n")
	}

	for i := range types {
		item := &types[i]
		if item.Create != nil {
			renderCreate(&buf, item)
		}
		if item.Patch != nil {
			renderPatch(&buf, item)
		}
	}

	output, err := format.Source(buf.Bytes())
	if err != nil {
		return nil, fmt.Errorf("formatting generated mappers: %w\n%s", err, buf.String())
	}

	return output, nil
}

type generatedImport struct {
	Path  string
	Alias string
}

func generatedImports(types []generatedType, modulePath string) []generatedImport {
	importsByPath := map[string]generatedImport{}
	for i := range types {
		item := &types[i]
		gqlmodelPath := modulePath + "/internal/api/graphql/gqlmodel"
		importsByPath[gqlmodelPath] = generatedImport{
			Path: gqlmodelPath,
		}
		if item.NeedsAuth {
			authctxPath := modulePath + "/pkg/authctx"
			importsByPath[authctxPath] = generatedImport{
				Path: authctxPath,
			}
		}
		if item.NeedsPulID || item.Create != nil {
			pulidPath := modulePath + "/shared/pulid"
			importsByPath[pulidPath] = generatedImport{
				Path: pulidPath,
			}
		}
		if item.NeedsDomain {
			importItem := generatedImport{Path: item.Binding.ImportPath}
			if filepath.Base(item.Binding.ImportPath) != item.Binding.PackageName {
				importItem.Alias = item.Binding.PackageName
			}
			importsByPath[item.Binding.ImportPath] = importItem
		}
		for _, importItem := range item.Imports {
			importsByPath[importItem.Path] = importItem
		}
	}

	imports := make([]generatedImport, 0, len(importsByPath))
	for _, item := range importsByPath {
		imports = append(imports, item)
	}
	sort.Slice(imports, func(i, j int) bool {
		return imports[i].Path < imports[j].Path
	})

	return imports
}

func renderCreate(buf *bytes.Buffer, item *generatedType) {
	create := item.Create
	fmt.Fprintf(
		buf,
		"func %s(\ninput gqlmodel.%s,\nid pulid.ID,\nauthCtx *authctx.AuthContext,\n) (*%s.%s, error) {\n",
		item.Name+"FromInput",
		create.InputName,
		item.Binding.PackageName,
		item.Binding.GoName,
	)
	for _, parse := range create.Parses {
		fmt.Fprintf(buf, "%s, err := %s\n", parse.VarName, parse.Expression)
		buf.WriteString("if err != nil {\n")
		buf.WriteString("return nil, err\n")
		buf.WriteString("}\n")
	}
	for _, defaultAssign := range create.Defaults {
		fmt.Fprintf(buf, "%s := %s\n", defaultAssign.VarName, defaultAssign.DefaultValue)
		fmt.Fprintf(buf, "if input.%s != nil {\n", defaultAssign.FieldName)
		fmt.Fprintf(buf, "%s = %s\n", defaultAssign.VarName, defaultAssign.InputOverride)
		buf.WriteString("}\n")
	}
	fmt.Fprintf(buf, "entity := &%s.%s{\n", item.Binding.PackageName, item.Binding.GoName)
	for _, field := range create.Fields {
		fmt.Fprintf(buf, "%s: %s,\n", field.FieldName, field.Expression)
	}
	buf.WriteString("}\n")
	buf.WriteString("return entity, nil\n")
	buf.WriteString("}\n\n")
}

func renderPatch(buf *bytes.Buffer, item *generatedType) {
	patch := item.Patch
	fmt.Fprintf(
		buf,
		"func Apply%sPatch(\nentity *%s.%s,\ninput gqlmodel.%s,\n) error {\n",
		item.Name,
		item.Binding.PackageName,
		item.Binding.GoName,
		patch.InputName,
	)
	for _, field := range patch.Fields {
		switch field.Guard {
		case "valueOK":
			fmt.Fprintf(
				buf,
				"if %s, ok := input.%s.ValueOK(); ok {\n",
				field.ValueName,
				field.FieldName,
			)
		case "nil":
			fmt.Fprintf(buf, "if input.%s != nil {\n", field.FieldName)
		}
		for _, line := range field.Body {
			buf.WriteString(line)
			buf.WriteString("\n")
		}
		buf.WriteString("}\n")
	}
	buf.WriteString("return nil\n")
	buf.WriteString("}\n\n")
}

func lowerFirst(value string) string {
	if value == "" {
		return ""
	}
	runes := []rune(value)
	runes[0] = unicode.ToLower(runes[0])
	return string(runes)
}

func camelToSnake(value string) string {
	if value == "" {
		return ""
	}

	var builder strings.Builder
	runes := []rune(value)
	for i, ch := range runes {
		if i > 0 && unicode.IsUpper(ch) {
			prev := runes[i-1]
			nextIsLower := i+1 < len(runes) && unicode.IsLower(runes[i+1])
			if unicode.IsLower(prev) || unicode.IsDigit(prev) || nextIsLower {
				builder.WriteByte('_')
			}
		}
		builder.WriteRune(unicode.ToLower(ch))
	}

	return builder.String()
}
