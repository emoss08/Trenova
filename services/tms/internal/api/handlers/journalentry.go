type JournalEntryHandlerParams struct {
	fx.In

	Service      *journalentryservice.Service
	PM           *middleware.PermissionMiddleware
	ErrorHandler *helpers.ErrorHandler
}

type JournalEntryHandler struct {
	service      *journalentryservice.Service
	pm           *middleware.PermissionMiddleware
	errorHandler *helpers.ErrorHandler
}

func NewJournalEntryHandler(p JournalEntryHandlerParams) *JournalEntryHandler {
	return &JournalEntryHandler{
		service:      p.Service,
		pm:           p.PM,
		errorHandler: p.ErrorHandler,
	}
}

func (h *JournalEntryHandler) RegisterRoutes(rg *gin.RouterGroup) {
	api := rg.Group("/journal-entries/")

	api.GET("", h.pm.RequirePermission(permission.ResourceJournalEntry, "read"), h.list)
	api.POST("", h.pm.RequirePermission(permission.ResourceJournalEntry, "create"), h.create)
	api.GET(
		"number/:entryNumber/",
		h.pm.RequirePermission(permission.ResourceJournalEntry, "read"),
		h.getByNumber,
	)
	api.GET(
		"reference/:referenceType/:referenceId/",
		h.pm.RequirePermission(permission.ResourceJournalEntry, "read"),
		h.getByReference,
	)
	api.GET(
		"period/:fiscalPeriodId/",
		h.pm.RequirePermission(permission.ResourceJournalEntry, "read"),
		h.getByPeriod,
	)
	api.GET(":id/", h.pm.RequirePermission(permission.ResourceJournalEntry, "read"), h.get)
	api.PUT(":id/", h.pm.RequirePermission(permission.ResourceJournalEntry, "update"), h.update)
	api.DELETE(
		":id/",
		h.pm.RequirePermission(permission.ResourceJournalEntry, "delete"),
		h.delete,
	)
	api.POST(
		":id/post/",
		h.pm.RequirePermission(permission.ResourceJournalEntry, "submit"),
		h.post,
	)
	api.POST(
		":id/approve/",
		h.pm.RequirePermission(permission.ResourceJournalEntry, "approve"),
		h.approve,
	)
	api.POST(
		":id/reject/",
		h.pm.RequirePermission(permission.ResourceJournalEntry, "reject"),
		h.reject,
	)
	api.POST(
		":id/reverse/",
		h.pm.RequirePermission(permission.ResourceJournalEntry, "submit"),
		h.reverse,
	)
}

func (h *JournalEntryHandler) list(c *gin.Context) {
	var isPosted, isApproved, isReversal, isAutoGenerated *bool

	if c.Query("isPosted") != "" {
		val := helpers.QueryBool(c, "isPosted")
		isPosted = &val
	}
	if c.Query("isApproved") != "" {
		val := helpers.QueryBool(c, "isApproved")
		isApproved = &val
	}
	if c.Query("isReversal") != "" {
		val := helpers.QueryBool(c, "isReversal")
		isReversal = &val
	}
	if c.Query("isAutoGenerated") != "" {
		val := helpers.QueryBool(c, "isAutoGenerated")
		isAutoGenerated = &val
	}

	pagination.Handle[*accounting.JournalEntry](c, context.GetAuthContext(c)).
		WithErrorHandler(h.errorHandler).
		Execute(func(
			c *gin.Context,
			opts *pagination.QueryOptions,
		) (*pagination.ListResult[*accounting.JournalEntry], error) {
			return h.service.List(c.Request.Context(), &repositories.ListJournalEntryRequest{
				Filter: opts,
				FilterOptions: repositories.JournalEntryFilterOptions{
					IncludeFiscalYear:   helpers.QueryBool(c, "includeFiscalYear"),
					IncludeFiscalPeriod: helpers.QueryBool(c, "includeFiscalPeriod"),
					IncludeLines:        helpers.QueryBool(c, "includeLines"),
					IncludePostedBy:     helpers.QueryBool(c, "postedBy"),
					IncludeApprovedBy:   helpers.QueryBool(c, "approvedBy"),
					Status:              helpers.QueryString(c, "status", ""),
					EntryType:           helpers.QueryString(c, "entryType", ""),
					FiscalYearID:        helpers.QueryString(c, "fiscalYearId", ""),
					FiscalPeriodID:      helpers.QueryString(c, "fiscalPeriodId", ""),
					ReferenceType:       helpers.QueryString(c, "referenceType", ""),
					ReferenceID:         helpers.QueryString(c, "referenceId", ""),
					IsPosted:            isPosted,
					IsApproved:          isApproved,
					IsReversal:          isReversal,
					IsAutoGenerated:     isAutoGenerated,
					StartDate:           helpers.QueryInt64(c, "startDate", 0),
					EndDate:             helpers.QueryInt64(c, "endDate", 0),
				},
			})
		})
}

func (h *JournalEntryHandler) get(c *gin.Context) {
	authCtx := context.GetAuthContext(c)

	id, err := pulid.MustParse(c.Param("id"))
	if err != nil {
		h.errorHandler.HandleError(c, err)
		return
	}

	entity, err := h.service.Get(
		c.Request.Context(),
		&repositories.GetJournalEntryByIDRequest{
			JournalEntryID: id,
			OrgID:          authCtx.OrganizationID,
			BuID:           authCtx.BusinessUnitID,
			UserID:         authCtx.UserID,
			FilterOptions: repositories.JournalEntryFilterOptions{
				IncludeFiscalYear:   helpers.QueryBool(c, "includeFiscalYear", false),
				IncludeFiscalPeriod: helpers.QueryBool(c, "includeFiscalPeriod", false),
				IncludeLines:        helpers.QueryBool(c, "includeLines", true),
				IncludePostedBy:     helpers.QueryBool(c, "includePostedBy", false),
				IncludeApprovedBy:   helpers.QueryBool(c, "includeApprovedBy", false),
			},
		},
	)
	if err != nil {
		h.errorHandler.HandleError(c, err)
		return
	}

	c.JSON(http.StatusOK, entity)
}

