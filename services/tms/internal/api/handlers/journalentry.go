type CreateJournalEntryRequest struct {
	Entry *accounting.JournalEntry       `json:"entry" binding:"required"`
	Lines []*accounting.JournalEntryLine `json:"lines" binding:"required"`
}

func (h *JournalEntryHandler) create(c *gin.Context) {
	authCtx := context.GetAuthContext(c)

	var req CreateJournalEntryRequest
	if err := c.ShouldBindJSON(&req); err != nil {
		h.errorHandler.HandleError(c, err)
		return
	}

	req.Entry.OrganizationID = authCtx.OrganizationID
	req.Entry.BusinessUnitID = authCtx.BusinessUnitID

	createdEntity, err := h.service.Create(
		c.Request.Context(),
		req.Entry,
		req.Lines,
		authCtx.UserID,
	)
	if err != nil {
		h.errorHandler.HandleError(c, err)
		return
	}

	c.JSON(http.StatusCreated, createdEntity)
}

