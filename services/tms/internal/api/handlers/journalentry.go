type ReverseJournalEntryRequest struct {
	ReversalReason string `json:"reversalReason" binding:"required"`
	ReversalDate   int64  `json:"reversalDate"`
}

func (h *JournalEntryHandler) reverse(c *gin.Context) {
	authCtx := context.GetAuthContext(c)

	id, err := pulid.MustParse(c.Param("id"))
	if err != nil {
		h.errorHandler.HandleError(c, err)
		return
	}

	var req ReverseJournalEntryRequest
	if err = c.ShouldBindJSON(&req); err != nil {
		h.errorHandler.HandleError(c, err)
		return
	}

	// Use current time if reversal date not provided
	if req.ReversalDate == 0 {
		req.ReversalDate = utils.NowUnix()
	}

	reversalEntry, err := h.service.Reverse(
		c.Request.Context(),
		&repositories.ReverseJournalEntryRequest{
			JournalEntryID: id,
			OrgID:          authCtx.OrganizationID,
			BuID:           authCtx.BusinessUnitID,
			UserID:         authCtx.UserID,
			ReversalDate:   req.ReversalDate,
			ReversalReason: req.ReversalReason,
		},
	)
	if err != nil {
		h.errorHandler.HandleError(c, err)
		return
	}

	c.JSON(http.StatusOK, reversalEntry)
}
