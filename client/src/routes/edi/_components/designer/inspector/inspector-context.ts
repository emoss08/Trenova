import type { BadgeVariant } from "@/components/ui/badge";
import type {
  EDIDocumentPreview,
  EDIMessage,
  EDIMessageInspection,
  InspectX12Request,
} from "@/types/edi";
import { formatUnix } from "../../edi-display-utils";
import { buildMessageJsonFilename, buildX12Filename } from "../utils/edi-message-utils";
import type { InspectorGridRow } from "./components/inspector-grid";
import { controlNumberText } from "./inspector-utils";

type PreviewTransactionSet = NonNullable<EDIDocumentPreview["profile"]>["transactionSet"];

export type InspectorContext = {
  title: string;
  subtitle: string;
  status?: {
    label: string;
    variant: BadgeVariant;
  };
  rawX12: string;
  rawFilename: string;
  controlRows: InspectorGridRow[];
  controlCopyText: string;
  overviewRows: InspectorGridRow[];
  payload?: {
    value: unknown;
    filename: string;
  };
  provenanceRows?: InspectorGridRow[];
};

export function buildMessageInspectorContext({
  message,
  provenance,
}: EDIMessageInspection): InspectorContext {
  return {
    title: `Message ${message.transactionControlNumber || message.id}`,
    subtitle: `${message.transactionSet} ${message.direction} generated ${formatUnix(message.generatedAt)}`,
    status: {
      label: message.status,
      variant: message.status === "Generated" ? "active" : "inactive",
    },
    rawX12: message.rawX12,
    rawFilename: buildX12Filename(message),
    controlRows: [
      ["Interchange Control Number", message.interchangeControlNumber],
      ["Group Control Number", message.groupControlNumber],
      ["Transaction Control Number", message.transactionControlNumber],
      ["Segment Count", String(message.segmentCount)],
    ],
    controlCopyText: controlNumberText({
      interchangeControlNumber: message.interchangeControlNumber,
      groupControlNumber: message.groupControlNumber,
      transactionControlNumber: message.transactionControlNumber,
    }),
    overviewRows: [
      ["Status", message.status],
      ["Partner", message.partner?.name ?? message.partnerDocumentProfile?.partner?.name ?? "-"],
      [
        "Document Type",
        message.documentType?.name ?? message.partnerDocumentProfile?.documentType?.name ?? "-",
      ],
      ["Transaction Set", message.transactionSet],
      ["Direction", message.direction],
      ["X12 Version", message.x12Version],
      ["Generated At", formatUnix(message.generatedAt)],
      ["Generated By ID", message.generatedById ?? "-"],
      ["Shipment ID", message.shipmentId ?? "-"],
      ["Transfer ID", message.transferId ?? "-"],
      ["Profile", message.partnerDocumentProfile?.name ?? message.partnerDocumentProfileId],
      [
        "Template",
        message.template?.name ??
          message.partnerDocumentProfile?.template?.name ??
          message.templateId,
      ],
      ["Template Version", messageVersionLabel(message)],
      ["Validation Mode", message.validationMode],
    ],
    payload: {
      value: message.payloadSnapshot ?? {},
      filename: buildMessageJsonFilename(message),
    },
    provenanceRows: [
      ["Message ID", provenance.messageId],
      ["Profile ID", provenance.profileId],
      ["Profile Name", message.partnerDocumentProfile?.name ?? "-"],
      ["Template ID", provenance.templateId],
      [
        "Template Name",
        message.template?.name ?? message.partnerDocumentProfile?.template?.name ?? "-",
      ],
      ["Template Version ID", provenance.templateVersionId],
      ["Template Version", messageVersionLabel(message)],
      ["Template Version Status", message.templateVersion?.status ?? "-"],
      ["Script Libraries", scriptLibraryLabel(message)],
      ["Source X12 Version", message.templateVersion?.x12Version ?? message.x12Version],
      ["Validation Mode", message.validationMode],
    ],
  };
}

export function buildPreviewInspectorContext(preview: EDIDocumentPreview): InspectorContext {
  const transactionSet = previewTransactionSet(preview);
  const direction =
    preview.profile?.direction ?? preview.profile?.template?.direction ?? "Outbound";
  const templateVersion = preview.templateVersion ?? preview.profile?.templateVersion;
  const templateVersionLabel = templateVersion ? `v${templateVersion.versionNumber}` : "-";

  return {
    title: `Preview ${preview.transactionControlNumber}`,
    subtitle: `${transactionSet || "X12"} ${direction} provisional preview`,
    status: {
      label: "Provisional",
      variant: "info",
    },
    rawX12: preview.rawX12,
    rawFilename: `edi-preview-${transactionSet || "x12"}-${preview.transactionControlNumber || "provisional"}.x12`,
    controlRows: [
      ["Interchange Control Number (Provisional)", preview.interchangeControlNumber],
      ["Group Control Number (Provisional)", preview.groupControlNumber],
      ["Transaction Control Number (Provisional)", preview.transactionControlNumber],
      ["Segment Count", String(preview.segmentCount)],
    ],
    controlCopyText: controlNumberText({
      interchangeControlNumber: preview.interchangeControlNumber,
      groupControlNumber: preview.groupControlNumber,
      transactionControlNumber: preview.transactionControlNumber,
    }),
    overviewRows: [
      ["Status", "Provisional"],
      ["Partner", preview.profile?.partner?.name ?? "-"],
      ["Document Type", preview.profile?.documentType?.name ?? "-"],
      ["Transaction Set", transactionSet || "-"],
      ["Direction", direction],
      ["X12 Version", preview.x12Version],
      ["Generated At", "Not archived"],
      ["Generated By ID", "-"],
      ["Shipment ID", "-"],
      ["Transfer ID", "-"],
      ["Profile", preview.profile?.name ?? "-"],
      ["Template", preview.profile?.template?.name ?? "-"],
      ["Template Version", templateVersionLabel],
      ["Validation Mode", preview.profile?.validationMode ?? "-"],
    ],
  };
}

export function buildPreviewInspectRequest(preview: EDIDocumentPreview): InspectX12Request {
  const transactionSet = previewTransactionSet(preview);
  return {
    rawX12: preview.rawX12,
    transactionSet: transactionSet || undefined,
    x12Version: preview.x12Version,
    envelope: preview.profile?.envelope ?? undefined,
    diagnostics: preview.diagnostics,
  };
}

function previewTransactionSet(preview: EDIDocumentPreview) {
  return (
    preview.profile?.transactionSet ??
    preview.profile?.template?.transactionSet ??
    preview.profile?.documentType?.transactionSet ??
    templateVersionTransactionSet(preview)
  );
}

function templateVersionTransactionSet(preview: EDIDocumentPreview) {
  return (preview.templateVersion as { transactionSet?: PreviewTransactionSet } | null)
    ?.transactionSet;
}

function messageVersionLabel(message: EDIMessage) {
  if (message.templateVersion?.versionNumber) {
    return `v${message.templateVersion.versionNumber}`;
  }
  if (message.partnerDocumentProfile?.templateVersion?.versionNumber) {
    return `v${message.partnerDocumentProfile.templateVersion.versionNumber}`;
  }
  return message.templateVersionId;
}

function scriptLibraryLabel(message: EDIMessage) {
  const libraries = message.templateVersion?.scriptLibraries ?? [];
  if (libraries.length === 0) return "-";
  return libraries
    .map((library) => {
      const functions =
        library.functionNames.length > 0 ? ` (${library.functionNames.join(", ")})` : "";
      return `${library.name}${functions}`;
    })
    .join("; ");
}
