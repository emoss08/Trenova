const SOURCE_TYPE_LABELS: Record<string, string> = {
  CustomerPayment: "Customer Payment",
  customer_payment: "Customer Payment",
  invoice: "Invoice",
  invoice_posted: "Invoice",
  manual_journal: "Manual Journal",
  journal_reversal: "Journal Reversal",
  shipment: "Shipment",
};

function humanizeSourceType(type: string): string {
  const known = SOURCE_TYPE_LABELS[type];
  if (known) return known;
  return type
    .replace(/_/g, " ")
    .replace(/([a-z])([A-Z])/g, "$1 $2")
    .replace(/\b\w/g, (char) => char.toUpperCase());
}

type AccountNet = {
  glAccountId: string;
  accountCode: string;
  accountName: string;
  debitMinor: number;
  creditMinor: number;
  netMinor: number;
};

function computeAccountNets(entries: PostingEntry[]): AccountNet[] {
  const byAccount = new Map<string, AccountNet>();
  for (const entry of entries) {
    for (const line of entry.lines ?? []) {
      let net = byAccount.get(line.glAccountId);
      if (!net) {
        net = {
          glAccountId: line.glAccountId,
          accountCode: line.glAccount?.accountCode ?? line.glAccountId,
          accountName: line.glAccount?.name ?? "",
          debitMinor: 0,
          creditMinor: 0,
          netMinor: 0,
        };
        byAccount.set(line.glAccountId, net);
      }
      net.debitMinor += line.debitAmount;
      net.creditMinor += line.creditAmount;
      net.netMinor += line.debitAmount - line.creditAmount;
    }
  }
  return [...byAccount.values()].sort((a, b) => a.accountCode.localeCompare(b.accountCode));
}

export function SourceDrillDownPage() {
  const { type, sourceId } = useParams<{ type: string; sourceId: string }>();
  const navigate = useNavigate();
  const sourceLabel = humanizeSourceType(type ?? "");

  const { data: entries, isLoading } = useQuery({
    ...queries.journalEntry.bySource(type!, sourceId!),
    enabled: !!type && !!sourceId,
  });
  const { data: sourceInfo } = useQuery({
    ...queries.journalEntry.sourceByObject(type!, sourceId!),
    enabled: !!type && !!sourceId,
  });

  const postings = useMemo(() => (entries ?? []) as PostingEntry[], [entries]);

  const summary = useMemo(() => {
    const totalDebit = postings.reduce((sum, entry) => sum + entry.totalDebit, 0);
    const totalCredit = postings.reduce((sum, entry) => sum + entry.totalCredit, 0);
    const accountNets = computeAccountNets(postings);
    const hasReversal = postings.some((entry) => entry.isReversal);
    const fullyReversed =
      hasReversal && accountNets.length > 0 && accountNets.every((net) => net.netMinor === 0);
    const allBalanced = postings.every((entry) => entry.totalDebit === entry.totalCredit);
    return { totalDebit, totalCredit, accountNets, hasReversal, fullyReversed, allBalanced };
  }, [postings]);

  return (
    <PageLayout
      pageHeaderProps={{
        title: `GL Postings — ${sourceLabel}`,
        description: `Every journal entry generated by this ${sourceLabel.toLowerCase()}, with line-level account detail and the net ledger impact.`,
      }}
    >
      <div className="mx-4 mt-3 mb-4 space-y-4">
        <div className="flex flex-wrap items-center gap-3">
          <Button variant="outline" size="sm" onClick={() => void navigate(-1)}>
            <ArrowLeftIcon className="size-3.5" />
            Back
          </Button>
          <SourceDrillDownLink
            sourceType={type!}
            sourceId={sourceId!}
            label={`Open ${sourceLabel}`}
          />
          {sourceInfo?.sourceDocumentNumber ? (
            <span className="inline-flex items-center rounded-md border bg-muted/50 px-2 py-0.5 font-mono text-xs text-muted-foreground">
              {sourceInfo.sourceDocumentNumber}
            </span>
          ) : null}
        </div>

        {isLoading ? (
          <>
            <div className="grid gap-2.5 md:grid-cols-4">
              {Array.from({ length: 4 }).map((_, i) => (
                <Skeleton key={i} className="h-20 rounded-lg" />
              ))}
            </div>
            <Skeleton className="h-64 w-full rounded-md" />
          </>
        ) : postings.length === 0 ? (
          <div className="flex justify-center pt-8">
            <EmptyState
              title="No journal entries"
              description={`Nothing has been posted to the general ledger for this ${sourceLabel.toLowerCase()} yet.`}
              icons={[ScrollTextIcon, BookOpenIcon]}
            />
          </div>
        ) : (
          <>
            <div className="grid grid-cols-2 gap-2.5 md:grid-cols-4">
              <SummaryTile
                index={0}
                label="Journal Entries"
                value={String(postings.length)}
                detail={summary.hasReversal ? "includes reversal" : "original posting"}
              />
              <SummaryTile
                index={1}
                label="Total Debits"
                value={<AmountDisplay value={summary.totalDebit} />}
              />
              <SummaryTile
                index={2}
                label="Total Credits"
                value={<AmountDisplay value={summary.totalCredit} />}
              />
              <SummaryTile
                index={3}
                label="Ledger Status"
                value={
                  summary.fullyReversed ? (
                    <span className="inline-flex items-center gap-1.5 text-orange-600 dark:text-orange-400">
                      Fully reversed
                    </span>
                  ) : summary.allBalanced ? (
                    <span className="inline-flex items-center gap-1.5 text-emerald-600 dark:text-emerald-400">
                      <CheckCircle2Icon className="size-4" />
                      Balanced
                    </span>
                  ) : (
                    <span className="text-red-600 dark:text-red-400">Out of balance</span>
                  )
                }
                detail={
                  summary.fullyReversed
                    ? "net account impact is zero"
                    : "debits equal credits per entry"
                }
              />
            </div>

            {postings.length > 1 && summary.accountNets.length > 0 ? (
              <Card className="gap-0 rounded-md py-0">
                <CardContent className="px-0">
                  <div className="flex items-center justify-between px-4 py-2.5">
                    <p className="text-sm font-medium">Net impact by account</p>
                    <p className="text-xs text-muted-foreground">
                      across {postings.length} entries
                    </p>
                  </div>
                  <div className="overflow-hidden border-t">
                    <table className="w-full text-sm">
                      <thead className="bg-muted/50 text-left text-muted-foreground">
                        <tr>
                          <th className="px-4 py-2 text-xs font-medium">Account</th>
                          <th className="px-4 py-2 text-right text-xs font-medium">Debits</th>
                          <th className="px-4 py-2 text-right text-xs font-medium">Credits</th>
                          <th className="px-4 py-2 text-right text-xs font-medium">Net</th>
                        </tr>
                      </thead>
                      <tbody>
                        {summary.accountNets.map((net) => (
                          <tr key={net.glAccountId} className="border-t">
                            <td className="px-4 py-2">
                              <span className="flex flex-col">
                                <span className="font-mono text-xs">{net.accountCode}</span>
                                {net.accountName ? (
                                  <span className="text-2xs text-muted-foreground">
                                    {net.accountName}
                                  </span>
                                ) : null}
                              </span>
                            </td>
                            <td className="px-4 py-2 text-right">
                              <AmountDisplay value={net.debitMinor} className="text-xs" />
                            </td>
                            <td className="px-4 py-2 text-right">
                              <AmountDisplay value={net.creditMinor} className="text-xs" />
                            </td>
                            <td className="px-4 py-2 text-right">
                              <span
                                className={cn(
                                  "text-xs font-medium tabular-nums",
                                  net.netMinor === 0 && "text-muted-foreground",
                                )}
                              >
                                {net.netMinor === 0 ? (
                                  "—"
                                ) : (
                                  <AmountDisplay
                                    value={net.netMinor}
                                    className="text-xs font-medium"
                                  />
                                )}
                              </span>
                            </td>
                          </tr>
                        ))}
                      </tbody>
                    </table>
                  </div>
                </CardContent>
              </Card>
            ) : null}

            <div className="space-y-2.5">
              {postings.map((entry, index) => (
                <m.div
                  key={entry.id}
                  initial={{ opacity: 0, y: 8 }}
                  animate={{ opacity: 1, y: 0 }}
                  transition={{ duration: 0.3, delay: index * 0.05, ease: "easeOut" }}
                >
                  <JournalEntryPostingCard entry={entry} />
                </m.div>
              ))}
            </div>
          </>
        )}
      </div>
    </PageLayout>
  );
}

function SummaryTile({
  index,
  label,
  value,
  detail,
}: {
  index: number;
  label: string;
  value: ReactNode;
  detail?: string;
}) {
  return (
    <m.div
      initial={{ opacity: 0, y: 8 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.3, delay: index * 0.05, ease: "easeOut" }}
    >
      <Card className="h-full gap-0 rounded-lg py-3">
        <CardContent className="px-4">
          <p className="text-[11px] font-medium tracking-wide text-muted-foreground uppercase">
            {label}
          </p>
          <p className="mt-1 text-lg font-semibold tracking-tight tabular-nums">{value}</p>
          {detail ? <p className="mt-0.5 text-xs text-muted-foreground">{detail}</p> : null}
        </CardContent>
      </Card>
    </m.div>
  );
}
