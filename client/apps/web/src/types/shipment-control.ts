import { z } from "zod";
import {
  nullableStringSchema,
  optionalStringSchema,
  timestampSchema,
  versionSchema,
} from "./helpers";

export const shipmentControlSchema = z
  .object({
    id: optionalStringSchema,
    version: versionSchema,
    createdAt: timestampSchema,
    updatedAt: timestampSchema,
    organizationId: optionalStringSchema,
    businessUnitId: optionalStringSchema,

    maxShipmentWeightLimit: z
      .number()
      .int()
      .min(0, { message: "Max shipment weight limit must be non-negative" }),
    autoDelayShipments: z.boolean(),
    autoDelayShipmentsThreshold: z.number().int().nullish(),
    detentionThreshold: z.number().int().nullish(),
    autoCancelShipments: z.boolean(),
    autoCancelShipmentsThreshold: z.number().int().nullish(),
    trackDetentionTime: z.boolean(),
    autoGenerateDetentionCharges: z.boolean(),
    detentionChargeId: nullableStringSchema,
    trackCustomerRejections: z.boolean(),
    checkForDuplicateBols: z.boolean(),
    allowMoveRemovals: z.boolean(),
    checkHazmatSegregation: z.boolean(),
  })
  .refine(
    (data) => {
      if (
        data.autoDelayShipments &&
        (!data.autoDelayShipmentsThreshold ||
          data.autoDelayShipmentsThreshold <= 0)
      ) {
        return false;
      }
      return true;
    },
    {
      path: ["autoDelayShipmentsThreshold"],
      message:
        "Delay threshold must be greater than 0 when auto delay is enabled",
    },
  )
  .refine(
    (data) => {
      if (data.trackDetentionTime && !data.detentionChargeId) {
        return false;
      }
      return true;
    },
    {
      path: ["detentionChargeId"],
      message: "Detention charge is required when tracking detention time",
    },
  )
  .refine(
    (data) => {
      if (
        data.trackDetentionTime &&
        (!data.detentionThreshold || data.detentionThreshold <= 0)
      ) {
        return false;
      }
      return true;
    },
    {
      path: ["detentionThreshold"],
      message:
        "Detention threshold must be greater than 0 when tracking detention time",
    },
  )
  .refine(
    (data) => {
      if (
        data.autoCancelShipments &&
        (!data.autoCancelShipmentsThreshold ||
          data.autoCancelShipmentsThreshold <= 0)
      ) {
        return false;
      }
      return true;
    },
    {
      path: ["autoCancelShipmentsThreshold"],
      message:
        "Auto cancel threshold must be greater than 0 when auto cancel is enabled",
    },
  );

export type ShipmentControl = z.infer<typeof shipmentControlSchema>;
