import { OrganizationType } from "@/types/organization";
import { InferType, mixed, object, string } from "yup";

export const organizationSchema = object({
  name: string().required("Name is required"),
  scacCode: string().required("SCAC code is required"),
  dotNumber: string().required("DOT number is required"),
  logoUrl: string().optional(),
  orgType: mixed<OrganizationType>()
    .required("Organization type is required")
    .oneOf(Object.values(OrganizationType)),
  bucketName: string().optional(), // Should not be required because it is generated by the system
  addressLine1: string().required("Address line 1 is required"),
  addressLine2: string().optional(),
  city: string().required("City is required"),
  stateId: string().required("State is required"),
  postalCode: string().required("Postal code is required"),
  timezone: string().required("Timezone is required"),
  taxId: string().optional(),
});

export type OrganizationSchema = InferType<typeof organizationSchema>;
